#include <iostream>

/*
    Напишете рекурсивна функция `int sum(int a, int b)`, която намира сумата на числата във затворения интервал [a, b].
*/

int sum(int a, int b)
{
    // Терминиращо условие. Сумата на празен интервал е 0.
    if (a > b)
        return 0;

    // Сумата на числата в интервала [a, b] е равна на а + сумата на числата в интервала [a + 1, b]
    return a + sum(a + 1, b);
}

int main()
{

    std::cout << sum(1, 10);

    return 0;
}