#include <iostream>

/*
    Напишете рекурсивна функция, която връща n-тото число на Фибоначи.
*/

int fib(int n)
{
    // Първото число на Фибоначи е 0
    if (n <= 1)
        return 0;
    // Второто число на Фибоначи е 1
    if (n == 2)
        return 1;

    // n-тото число на Фибоначи е сумата на (n - 1)-то и (n - 2)-то
    return fib(n - 1) + fib(n - 2);
}

int main()
{

    std::cout << fib(7);

    return 0;
}