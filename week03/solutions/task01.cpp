#include <iostream>

/*
    Да се напише програма, която по въвдено естествено число n отпечатва числата от 1 до n, които се делят на 3.
*/
int main()
{

    int n = 1;
    std::cin >> n;

    // Итерираме по всички числа от 1 до n
    for (int i = 1; i <= n; i++)
    {   
        // На всяка итерация i ще има нова стойност.
        if (i % 3 == 0)
            std::cout << i << std::endl;
    }

    return 0;
}